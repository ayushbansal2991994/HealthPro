package bean;

public class Supplement {
	
	private String fat_loss;
	private String maintain;
	private String gain_weight;
	
	
	public void setFat_loss(String fat_loss) {
		this.fat_loss = fat_loss;
	}
	public String getFat_loss() {
		return fat_loss;
	}
	public void setMaintain(String maintain) {
		this.maintain = maintain;
	}
	public String getMaintain() {
		return maintain;
	}
	public void setGain_weight(String gain_weight) {
		this.gain_weight = gain_weight;
	}
	public String getGain_weight() {
		return gain_weight;
	}
	
	
}
